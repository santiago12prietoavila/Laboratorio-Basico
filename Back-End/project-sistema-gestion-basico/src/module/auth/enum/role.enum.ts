// Definición de la enumeración ERole
export enum ERole {
    // Rol de usuario estándar, representado como una cadena 'user'.
    USER = 'user',

    // Rol de administrador, representado como una cadena "admin"
    ADMIN = 'admin',

    // Rol de moderador, representado como una cadena "moderator".
    MODERATOR = 'moderator',
}